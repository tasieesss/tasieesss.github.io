// Глобальные переменные
let currentQuestion = 0;
let answers = [];
let organizationName = '';
let userEmail = '';
let totalScore = 0;
let scoresByCriterion = {};

// Функция для запуска теста
function startTest() {
    organizationName = document.getElementById('organization').value.trim();
    userEmail = document.getElementById('email').value.trim();

    if (!organizationName || !userEmail) {
        alert('Будь ласка, заповніть всі поля');
        return;
    }

    if (!validateEmail(userEmail)) {
        alert('Будь ласка, введіть коректну електронну адресу');
        return;
    }

    // Инициализируем scoresByCriterion с ключами-строками!
    for (let i = 0; i < testData.criteria.length; i++) {
        scoresByCriterion[testData.criteria[i]] = { score: 0, maxScore: 0 };
    }

    document.getElementById('start-page').classList.add('hidden');
    document.getElementById('test-page').classList.remove('hidden');
    showQuestion(currentQuestion);
}

// Функция для валидации email
function validateEmail(email) {
    const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return re.test(email);
}

// Функция для отображения вопроса
function showQuestion(index) {
    const question = testData.questions[index];
    const container = document.getElementById('question-container');

    container.innerHTML = `
        <div class="question-container">
            <div class="question">
                <span class="question-number">Питання ${index + 1}/${testData.questions.length}</span><br>
                ${question.text}
            </div>
            <div class="options">
                ${question.options.map((option, optIndex) => `
                    <label class="option">
                        <input type="radio" name="question_${index}" value="${option.value}" onchange="enableNext()">
                        <span>${option.text}</span>
                    </label>
                `).join('')}
            </div>
        </div>
    `;

    updateProgress();
    document.getElementById('next-btn').disabled = true;

    if (index === testData.questions.length - 1) {
        document.getElementById('next-btn').classList.add('hidden');
        document.getElementById('finish-btn').classList.remove('hidden');
        document.getElementById('finish-btn').disabled = true;
    } else {
        document.getElementById('next-btn').classList.remove('hidden');
        document.getElementById('finish-btn').classList.add('hidden');
    }
}

// Функция для обновления прогресс-бара
function updateProgress() {
    const progress = ((currentQuestion + 1) / testData.questions.length) * 100;
    document.getElementById('progress').style.width = progress + '%';
}

// Функция для включения кнопки "Далее" или "Завершить"
function enableNext() {
    // Только на последнем вопросе активируем finish-btn, на остальных next-btn
    if (currentQuestion === testData.questions.length - 1) {
        document.getElementById('finish-btn').disabled = false;
    } else {
        document.getElementById('next-btn').disabled = false;
    }
}

// Функция для перехода к следующему вопросу
function nextQuestion() {
    saveAnswer();
    currentQuestion++;
    if (currentQuestion < testData.questions.length) {
        showQuestion(currentQuestion);
    }
}

// Функция для сохранения ответа
function saveAnswer() {
    const selectedOption = document.querySelector(`input[name="question_${currentQuestion}"]:checked`);
    if (selectedOption) {
        answers[currentQuestion] = parseInt(selectedOption.value);
    }
}

// Функция для завершения теста и отображения результатов
function finishTest() {
    saveAnswer();
    calculateResults();
    displayResults();
    generateRecommendations();

    document.getElementById('test-page').classList.add('hidden');
    document.getElementById('results-page').classList.remove('hidden');
    document.getElementById('org-name').textContent = organizationName;
    createFloatingParticles();
}

// Функция для расчета результатов
function calculateResults() {
    totalScore = 0;
    scoresByCriterion = {}; // сбрасываем!

    testData.questions.forEach((question, index) => {
        const answerValue = answers[index] || 0;
        totalScore += answerValue;

        const criterionKey = question.criterion;
        if (!scoresByCriterion[criterionKey]) {
            scoresByCriterion[criterionKey] = { score: 0, maxScore: 0 };
        }
        scoresByCriterion[criterionKey].score += answerValue;
        scoresByCriterion[criterionKey].maxScore += Math.max(...question.options.map(o => o.value));
    });
}

function displayResults() {
    const tbody = document.getElementById('results-tbody');
    tbody.innerHTML = '';

    Object.keys(scoresByCriterion).forEach((criterionName) => {
        const criterionScore = scoresByCriterion[criterionName].score;
        const maxScore = scoresByCriterion[criterionName].maxScore;
        const level = getLevel(criterionScore, maxScore);
        const row = document.createElement('tr');
        row.innerHTML = `
            <td>${criterionName}</td>
            <td>-</td>
            <td>${maxScore}</td>
            <td>${criterionScore}</td>
            <td><span class="level-${level}">${getLevelText(level)}</span></td>
        `;
        tbody.appendChild(row);
    });
}

function generateRecommendations() {
    const recommendationsContainer = document.getElementById('recommendations-container');
    recommendationsContainer.innerHTML = '';
    
    Object.keys(scoresByCriterion).forEach((criterionName) => {
        const criterionScore = scoresByCriterion[criterionName].score;
        const maxScore = scoresByCriterion[criterionName].maxScore;
        const level = getLevel(criterionScore, maxScore);

        let recommendationText = '';
        if (
            testData.recommendations &&
            testData.recommendations[criterionName] &&
            testData.recommendations[criterionName][level]
        ) {
            recommendationText = testData.recommendations[criterionName][level];
        } else {
            recommendationText = 'Рекомендація для цього рівня не знайдена.';
        }

        const recommendationItem = document.createElement('div');
        recommendationItem.classList.add('recommendation-item');
        recommendationItem.innerHTML = `
            <div class="recommendation-title">Критерій: ${criterionName}</div>
            <p>${recommendationText}</p>
        `;
        recommendationsContainer.appendChild(recommendationItem);
    });
}

// Функция для определения уровня
function getLevel(score, maxScore) {
    if (maxScore === 0) return 'low';
    const percentage = (score / maxScore) * 100;
    if (percentage >= 70) {
        return 'high';
    } else if (percentage >= 40) {
        return 'medium';
    } else {
        return 'low';
    }
}

// Функция для получения текста уровня
function getLevelText(level) {
    if (level === 'high') return 'Високий';
    if (level === 'medium') return 'Середній';
    return 'Низький';
}

// Создание плавающих частиц для эффекта
function createFloatingParticles() {
    const container = document.createElement('div');
    container.classList.add('floating-particles');
    document.body.appendChild(container);

    const numberOfParticles = 20;
    for (let i = 0; i < numberOfParticles; i++) {
        const particle = document.createElement('div');
        particle.classList.add('particle');
        particle.style.left = Math.random() * 100 + '%';
        particle.style.top = Math.random() * 100 + '%';
        particle.style.animationDelay = Math.random() * 8 + 's';
        particle.style.animationDuration = (Math.random() * 3 + 5) + 's';
        container.appendChild(particle);
    }

    // Удаляем частицы через 10 секунд
    setTimeout(() => {
        if (container.parentNode) {
            container.parentNode.removeChild(container);
        }
    }, 10000);
}

